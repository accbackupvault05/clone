// Theme utilities
export const getBreakpoint = (breakpoint: keyof Theme['breakpoints']) => {
  return `@media (min-width: ${theme.breakpoints[breakpoint]})`;
};

export const rgba = (hex: string, alpha: number): string => {
  const r = parseInt(hex.slice(1, 3), 16);
  const g = parseInt(hex.slice(3, 5), 16);
  const b = parseInt(hex.slice(5, 7), 16);
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
};

// Responsive font size utility
export const responsiveFontSize = (mobile: string, tablet?: string, desktop?: string) => `
  font-size: ${mobile};
  ${tablet ? `@media (min-width: ${theme.breakpoints.tablet}) { font-size: ${tablet}; }` : ''}
  ${desktop ? `@media (min-width: ${theme.breakpoints.desktop}) { font-size: ${desktop}; }` : ''}
`;

// Responsive spacing utility
export const responsiveSpacing = (mobile: string, tablet?: string, desktop?: string) => `
  ${mobile};
  ${tablet ? `@media (min-width: ${theme.breakpoints.tablet}) { ${tablet}; }` : ''}
  ${desktop ? `@media (min-width: ${theme.breakpoints.desktop}) { ${desktop}; }` : ''}
`;